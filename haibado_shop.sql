
create table cart
(
    id      bigint generated by default as identity
        primary key,
    user_id bigint
        constraint fkgol9os2lyo1m4bu3aa5rg7jyl
            references customer
);

create table cart_item
(
    id         bigint generated by default as identity
        primary key,
    color      varchar(255),
    size       varchar(255),
    cart_id    bigint
        constraint fk1uobyhgl1wvgt1jpccia8xxs3
            references cart,
    product_id bigint
        constraint fkjcyd5wv4igqnw413rgxbfu4nv
            references product,
    quantity   integer not null
);

create table category
(
    id           bigint generated by default as identity
        primary key,
    product_type varchar(255)
);

create table color
(
    id    bigint generated by default as identity
        primary key,
    color varchar(255)
);

create table customer
(
    id           bigint generated by default as identity
        primary key,
    address      varchar(255),
    email        varchar(255),
    full_name    varchar(255),
    phone_number varchar(255)
);

create table discount
(
    id                  bigint generated by default as identity
        primary key,
    code                varchar(255),
    discount_date       timestamp(6),
    discount_percentage double precision
);

create table discount_id_product
(
    discount_id bigint not null
        constraint fk93avjhknuvywxotk49t0m551
            references discount,
    id_product  bigint
);

create table product
(
    id             bigint generated by default as identity
        primary key,
    description    varchar(255),
    name           varchar(255),
    original_price double precision not null,
    category_id    bigint
        constraint fk1mtsbur82frn64de7balymq9s
            references category,
    style          varchar(255)
);

create table "product-quantity"
(
    id         bigint generated by default as identity
        primary key,
    quantity   integer not null,
    color_id   bigint
        constraint fkgc7konqcueq09a8kiknyj8gn3
            references color,
    product_id bigint
        constraint fk2pmmdis2uxfgkqqecqae2to8w
            references product,
    size_id    bigint
        constraint fkrwkfi3jhrc1byk9o2gdv5ttcs
            references size
);

create table product_images
(
    product_id bigint not null
        constraint fki8jnqq05sk5nkma3pfp3ylqrt
            references product,
    images     varchar(255)
);

create table review
(
    id          bigint generated by default as identity
        primary key,
    rate        integer not null,
    review      varchar(255),
    product_id  bigint
        constraint fkiyof1sindb9qiqr9o8npj8klt
            references product,
    customer_id bigint
        constraint fkgce54o0p6uugoc2tev4awewly
            references customer
);

create table size
(
    id   bigint generated by default as identity
        primary key,
    size varchar(255)
);


